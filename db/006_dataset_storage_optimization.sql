BEGIN;

CREATE TABLE IF NOT EXISTS dataset_schema (
  id BIGSERIAL PRIMARY KEY,
  data_type TEXT NOT NULL,
  dataset_code TEXT NOT NULL,
  schema_hash TEXT NOT NULL,
  columns TEXT[] NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (data_type, dataset_code, schema_hash)
);

CREATE TABLE IF NOT EXISTS building_info_lookup (
  id BIGSERIAL PRIMARY KEY,
  release_id BIGINT NOT NULL REFERENCES cadastral_release(id) ON DELETE CASCADE,
  pnu TEXT NOT NULL,
  dataset_code TEXT NOT NULL,
  building_mgmt_pk TEXT,
  building_name TEXT,
  dong_name TEXT,
  building_name_norm TEXT,
  area_text TEXT,
  violation_raw TEXT,
  is_violation BOOLEAN,
  source_file TEXT,
  row_no BIGINT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS building_info_lookup_release_pnu_idx
  ON building_info_lookup (release_id, pnu);

CREATE INDEX IF NOT EXISTS building_info_lookup_release_pnu_dataset_idx
  ON building_info_lookup (release_id, pnu, dataset_code);

CREATE INDEX IF NOT EXISTS building_info_lookup_release_mgmt_pk_idx
  ON building_info_lookup (release_id, building_mgmt_pk);

DO $$
DECLARE
  table_exists BOOLEAN;
  partitioned_exists BOOLEAN;
BEGIN
  SELECT EXISTS (
    SELECT 1
    FROM pg_class c
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE n.nspname = 'public'
      AND c.relname = 'dataset_record'
      AND c.relkind IN ('r', 'p')
  ) INTO table_exists;

  IF NOT table_exists THEN
    CREATE TABLE dataset_record (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY,
      release_id BIGINT NOT NULL REFERENCES cadastral_release(id) ON DELETE CASCADE,
      data_type TEXT NOT NULL,
      dataset_code TEXT,
      source_file TEXT,
      row_no BIGINT,
      pnu TEXT,
      schema_id BIGINT REFERENCES dataset_schema(id) ON DELETE RESTRICT,
      payload_values JSONB,
      payload JSONB,
      geometry JSONB,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      PRIMARY KEY (release_id, id)
    ) PARTITION BY LIST (release_id);
    CREATE TABLE IF NOT EXISTS dataset_record_p_default
      PARTITION OF dataset_record DEFAULT;
  ELSE
    SELECT EXISTS (
      SELECT 1
      FROM pg_partitioned_table p
      JOIN pg_class c ON c.oid = p.partrelid
      JOIN pg_namespace n ON n.oid = c.relnamespace
      WHERE n.nspname = 'public'
        AND c.relname = 'dataset_record'
    ) INTO partitioned_exists;

    IF partitioned_exists THEN
      ALTER TABLE dataset_record
        ADD COLUMN IF NOT EXISTS schema_id BIGINT,
        ADD COLUMN IF NOT EXISTS payload_values JSONB;
      ALTER TABLE dataset_record
        ALTER COLUMN payload DROP NOT NULL;
      CREATE TABLE IF NOT EXISTS dataset_record_p_default
        PARTITION OF dataset_record DEFAULT;
    ELSE
      ALTER TABLE dataset_record RENAME TO dataset_record_legacy_tmp;

      CREATE TABLE dataset_record (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY,
        release_id BIGINT NOT NULL REFERENCES cadastral_release(id) ON DELETE CASCADE,
        data_type TEXT NOT NULL,
        dataset_code TEXT,
        source_file TEXT,
        row_no BIGINT,
        pnu TEXT,
        schema_id BIGINT REFERENCES dataset_schema(id) ON DELETE RESTRICT,
        payload_values JSONB,
        payload JSONB,
        geometry JSONB,
        created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
        PRIMARY KEY (release_id, id)
      ) PARTITION BY LIST (release_id);

      CREATE TABLE IF NOT EXISTS dataset_record_p_default
        PARTITION OF dataset_record DEFAULT;

      INSERT INTO dataset_record (
        id, release_id, data_type, dataset_code, source_file, row_no, pnu,
        schema_id, payload_values, payload, geometry, created_at
      )
      SELECT
        id, release_id, data_type, dataset_code, source_file, row_no, pnu,
        NULL::BIGINT, NULL::JSONB, payload, geometry, COALESCE(created_at, NOW())
      FROM dataset_record_legacy_tmp;

      DROP TABLE dataset_record_legacy_tmp;

      PERFORM setval(
        pg_get_serial_sequence('dataset_record', 'id'),
        COALESCE((SELECT MAX(id) FROM dataset_record), 1),
        TRUE
      );
    END IF;
  END IF;

  BEGIN
    ALTER TABLE dataset_record
      ADD CONSTRAINT dataset_record_schema_fk
      FOREIGN KEY (schema_id)
      REFERENCES dataset_schema(id)
      ON DELETE RESTRICT;
  EXCEPTION
    WHEN duplicate_object THEN
      NULL;
  END;
END $$;

CREATE INDEX IF NOT EXISTS dataset_record_release_pnu_idx
  ON dataset_record (release_id, pnu);

CREATE INDEX IF NOT EXISTS dataset_record_data_type_release_id_idx
  ON dataset_record (data_type, release_id, id DESC);

CREATE INDEX IF NOT EXISTS dataset_record_dataset_code_idx
  ON dataset_record (data_type, dataset_code, release_id, id DESC);

CREATE INDEX IF NOT EXISTS dataset_record_schema_release_idx
  ON dataset_record (schema_id, release_id);

CREATE OR REPLACE FUNCTION ensure_dataset_record_partition(target_release_id BIGINT)
RETURNS VOID
LANGUAGE plpgsql
AS $$
DECLARE
  part_name TEXT;
BEGIN
  IF target_release_id IS NULL THEN
    RETURN;
  END IF;

  part_name := format('dataset_record_r%s', target_release_id);

  IF NOT EXISTS (
    SELECT 1
    FROM pg_inherits i
    JOIN pg_class c ON c.oid = i.inhrelid
    JOIN pg_class p ON p.oid = i.inhparent
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE p.relname = 'dataset_record'
      AND n.nspname = 'public'
      AND c.relname = part_name
  ) THEN
    BEGIN
      EXECUTE format(
        'CREATE TABLE IF NOT EXISTS %I PARTITION OF dataset_record FOR VALUES IN (%s)',
        part_name,
        target_release_id
      );
    EXCEPTION
      WHEN duplicate_table THEN
        -- Concurrent workers may race on the same partition creation; one winner is enough.
        NULL;
    END;
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION drop_dataset_record_partition(target_release_id BIGINT)
RETURNS BOOLEAN
LANGUAGE plpgsql
AS $$
DECLARE
  part_name TEXT;
BEGIN
  IF target_release_id IS NULL THEN
    RETURN FALSE;
  END IF;

  part_name := format('dataset_record_r%s', target_release_id);

  IF EXISTS (
    SELECT 1
    FROM pg_inherits i
    JOIN pg_class c ON c.oid = i.inhrelid
    JOIN pg_class p ON p.oid = i.inhparent
    JOIN pg_namespace n ON n.oid = c.relnamespace
    WHERE p.relname = 'dataset_record'
      AND n.nspname = 'public'
      AND c.relname = part_name
  ) THEN
    EXECUTE format('DROP TABLE IF EXISTS %I', part_name);
    RETURN TRUE;
  END IF;

  RETURN FALSE;
END;
$$;

COMMIT;
